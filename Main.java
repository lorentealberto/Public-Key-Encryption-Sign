    private void cifrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cifrarActionPerformed
        if (core.clavesGeneradas()) {
            int returnVal = simple.showOpenDialog(this);
            if (returnVal == JFileChooser.APPROVE_OPTION) {
                File file = simple.getSelectedFile();
                core.cifrar(file, "RSA/ECB/PKCS1Padding");
            }
        } else
            consola.append("\nAntes de cifrar el archivo debes generar"
                    + "\nun par de claves.");
    }//GEN-LAST:event_cifrarActionPerformed

    private void descifrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_descifrarActionPerformed
        if (core.clavesGeneradas()) {
            int returnVal = algiz.showOpenDialog(this);
            if (returnVal == JFileChooser.APPROVE_OPTION) {
                File file = algiz.getSelectedFile();
                core.descifrar(file);
            }
        } else
            consola.append("\nAntes de cifrar el archivo debes generar"
                    + "\nun par de claves.");
    }//GEN-LAST:event_descifrarActionPerformed

    private void firmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_firmarActionPerformed
        if (core.clavesGeneradas()) {
            int returnVal = simple.showOpenDialog(this);
            if (returnVal == JFileChooser.APPROVE_OPTION) {
                File file = simple.getSelectedFile();
                core.firmar(file, keyOptions.getFirma());
            }
        } else
            consola.append("\nAntes de firmar el archivo debes generar"
                    + "\nun par de claves.");
            
    }//GEN-LAST:event_firmarActionPerformed

    private void verificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_verificarActionPerformed
        if (core.clavesGeneradas()) {
            int returnVal = raido.showOpenDialog(this);
            if (returnVal == JFileChooser.APPROVE_OPTION) {
                File file = raido.getSelectedFile();
                core.verificarFirma(file);
            }
        } else
            consola.append("\nAntes de verificar el archivo es necesario"
                    + "\nque exista un llavero");
    }//GEN-LAST:event_verificarActionPerformed

